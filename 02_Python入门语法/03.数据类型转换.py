#将数据类型转换为字符串
num_str = str(11)
print(type(num_str),num_str)
float_str = str(11.23)
print(type(float_str),float_str)

#将字符串转换成数字
str_int = int("11")
print(type(str_int),str_int)
str_float = float("11.23")
print(type(str_float),str_float)

